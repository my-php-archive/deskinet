<p>
				<center><strong class="size14">Protocolo de Turinga!</strong></center>
<br />
<br />
				Turinga! es la comunidad virtual donde millones de usuarios comparten, evalúan y seleccionan día a día la mejor información que encuentran o generan por sus propios medios de diversas temáticas (links, imágenes, noticias, videos, etc.). Este sitio tiene una funcionalidad del tipo web 2.0 (los usuarios generan el contenido).<br />
<br />
Con el objetivo de mantener toda la información publicada de la manera más limpia, ordenada y prolija posible se han creado varias reglas (protocolo) las cuales deben ser respetadas por todos los participantes del sitio.<br />
<br />

				Turinga! se encuentra dividido en dos grandes secciones, la primera y más antigua es llamada "Posts", la segunda y más nueva se denomina "Comunidades", en todo el sitio se aplican criterios comunes de convivencia, sin embargo, en cada sección cuenta ciertas "reglas" propias.<br />
<br />
A continuación se transcribe el protocolo:<br />
<br />
</p>

<p>
				<strong><u>Protocolo para Comunidades:</u></strong>
<br />
<br />
Una Comunidad es un espacio creado y administrado por usuarios con el objetivo generar un espacio de interacción entre personas. Estas pueden compartir sus ideas, intereses, dudas, gustos o cualquier tipo de afinidad con los demás.
<br />
<br />
Existen reglas y normas generales en las cuales se basa la filosofía de administración de Turinga! y que deben ser respetados por todas y cada una de las "Comunidades", así mismo, cada Comunidad podrá designar sus propias regalas y normas siempre y cuando estas no contradigan las que se establecen en este protocolo.
<br />
<br />
<h3><strong><u>De las comunidades y los temas:</u></strong></h3>
<br />
<br />
Se eliminarán las comunidades y/o temas que:
<br />
<br />
<ul>
	<li>Contengan links de descargas.</li>
	<li>Contengan sorteos o concursos por puntos y/o busquen el intercambio de puntos.</li>
	<li>Violen derechos de autor; responderemos a las denuncias positivamente sobre la infracción de la propiedad intelectual.</li>
	<li>Contenidos que fomenten o promulguen el odio o la discriminación en función de la raza, el origen étnico, la nacionalidad, la religión, el sexo o la orientación sexual o cualquier otro tipo.</li>
	<li>Material morboso tales como cadáveres, vómitos, violaciones, sangre, heridas, enfermedades, etc.</li>
<li>Contenido sexual explícito.</li>	<li>Contenido malicioso con el único objetivo de difamar, desprestigiar o injuriar la reputación de terceros (personal, empresarial, político, social, religioso, etc.).</li>
	<li>Promuevan el comportamiento violento.</li>
	<li>Robo de identidad y/o cualquier contenido falso que forme un material engañoso e/o irreal.</li>
	<li>Contengan información privada no autorizada de una persona, como el número de la tarjeta de crédito, número telefónico, dirección residencial u otro, el número del pasaporte, etc. O contengan passwords o accesos a información de terceros.</li>
	<li>Comunidades demasiado específicas y que no justifiquen la creación de la misma.</li>
	<li>Títulos en MAYUSCULA (parcialmente o totalmente) o {{{ QuE QUiErAn LLamaR la AtenCiÓN!!! }}}.</li>
	<li>Contenidos que incentiven actividades violentas, ilegales y/o peligrosas.</li>
	<li>Con el objetivo de hacer SPAM, malware, Referer y Phishing.</li>
	<li>Agreguen links con sistemas de referencia del tipo "marketing piramidal" con el fin de obtener beneficios personales.</li>
	<li>Comunidades sin contenido.</li>
	<li>Comunidades de un mismo tema creadas por el mismo usuario.</li>
	<li>Material que la administración de Taringa! encuentre no conveniente.</li>

</ul>
<br />



<strong>Contenido que SI esta permitido en "Comunidades" pero NO esta permitido en "Posts":</strong>
<br />
<br />
<ul>
	<li>Crear comunidades o temas relacionados con comercios y/o empresas siempre y cuando no tenga como objetivo realizar SPAM.</li>
	<li>Crear comunidades o temas que su tópico o contenido sea para consultas o preguntas.</li>
	<li>Crear comunidades o temas que su tópico o contenido relacionados con gustos o experiencias personales.</li>
	<li>Crear comunidades o temas con el objetivo de conocer las ideas de los participantes de la comunidad.</li>
	<li>Crear comunidades o temas que ya existan en Turinga! (Se permite que haya más de una comunidad sobre el mismo tema).</li>

</ul>

<p>
	<br />
<h3><strong><u>De los post:</u></strong></h3>
<br />
<br />
				<strong>Se eliminan los post que contengan:</strong>
<br />
<br />
<ul>
	<li>Material que ya fue posteado anteriormente (famosos reposts). Excepto descargas que pueden dejar de funcionar con el tiempo.</li>
	<li>Contenido de mala calidad, pobre o con errores en su creación.</li>
	<li>Títulos poco descriptivos, que sean CON MAYUSCULA (parcialmente o totalmente) o {{{ QuE QUiErAn LLamaR la AtenCiÓN!!! }}}.</li>
	<li>Noticias que no contienen fuente. (Es necesario agregar el link correspondiente).</li>
	<li>Links de Torrents, Pando, Emule, etc. (P2P, P2M).</li>
	<li>Material morboso tales como cadáveres, vómitos, violaciones, sangre, heridas, enfermedades, etc.</li>
	<li>Información personal propia o de terceros tales como e-mails, msn, nombres, teléfonos, etc.</li>
	<li>Menciones y/o referencias a promociones y/o publicidades de productos y/o servicios a excepción de aquellos autorizados por la Administración del sitio.</li>
<li>Contenido sexual explícito.</li>	<li>Links a páginas/blogs personales/propias/amigos (SPAM).</li>
	<li>Mensajes a otros usuarios o a los moderadores.</li>
	<li>Mensajes o contenido con el claro objetivo de buscar polémica (política, fútbol, etc.).</li>
	<li>Contenido que se relacione o haga "apología de delito" (drogas, violencia, delincuencia). La violencia es un delito.</li>
	<li>Passwords o accesos privados (Rapidshare, Megaupload, cuentas de correo, etc.).</li>
	<li>Sorteos no autorizados por un moderador. No pueden ser realizados por novatos.</li>
	<li>Quejas y/o opiniones sobre las reglas y/o funcionamiento y/o administración del sitio.</li>
	<li>Material que la administración de Turinga! encuentre no conveniente.</li>
</ul>
<br />
<br />
				<strong>Se cierran los post que contienen:</strong>
<br />
<br />
<ul>
				<li>Temas demasiados polémicos (políticos, religiosos, etc.) que pueden llegar a ofender a otros usuarios, ya sea por su contenido o comentarios.</li>
				<li>Posts donde se genera demasiada polémica agresiva (forobardo).</li>
</ul>
<br />
<br />
<h3><strong><u>De los comentarios (Posts) y las respuestas (Comunidades):</u></strong></h3>
<br />
<br />
				<strong>Se eliminan comentarios/respuestas que contengan:</strong>
<br />
<br />
<ul>
				<li>Mayúsculas o con abuso de mayúsculas.</li>
				<li>Insultos, ofensas, etc. (a otro usuario o de forma general).</li>
				<li>Comentarios racistas y/o peyorativos.</li>
				<li>Tipografías muy grandes o con el claro efecto de llamar la atención.</li>
				<li>Escaleras de emoticones/repetición de una o muchas imágenes en un mismo comentario.</li>
				<li>Con Flash (SFW) molestos o que emitan cualquier tipo de sonido.</li>
</ul>
<br />
<br />


<h3><strong><u>De los usuarios:</u></strong></h3>
<br />
<br />
				<strong>Se suspenderán de manera temporal o permanente a los usuarios que:</strong>
<br />
<br />
<ul>
				<li>Fomenten y/o publiquen a través de cualquier medio contenido que hagan referencia de manera directa o indirecta a la pedofilia</li>
				<li>Fomenten y/o publiquen a través de cualquier medio contenido que hagan referencia de manera directa o indirecta a actos morbosos.</li>
				<li>Fomenten y/o publiquen a través de cualquier medio contenido que hagan referencia de manera directa o indirecta actividades violentas, ilegales y/o peligrosas.</li>
				<li>Fomenten y/o publiquen a través de cualquier medio contenido que hagan referencia de manera directa o indirecta hagan apología de algún delito grave (matar, robar, corromper o dañar a otras personas).</li>
				<li>Fomenten y/o publiquen a través de cualquier medio contenido que hagan referencia de manera directa o indirecta al racismo y/o al odio. (Cualquier insulto racista, así sea de uso "común" en su ámbito de pertenecía.)</li>
				<li>Utilicen más de una cuenta personal sin importar el fin de las mismas.</li>
				<li>Insulten a otro usuario y/o moderador.</li>
				<li>Hagan a través de cualquier medio (Mensaje personal, Comunidad, Tema, Respuesta, Comentario o Post) promoción de sitios y/o productos ajenos a Turinga! y/o posts propios del usuario.(SPAM).</li>
				<li>Busquen generar polémica por medio de comentarios desafiantes o descalificantes.</li>
				<li>Hacen "escalerita de caritas" (comentarios donde sólo se agregan caritas para formar figuras).</li>
				<li>Agregan links con sistemas de referencia del tipo "marketing piramidal" con el fin de obtener beneficios personales (Referer).</li>
				<li>Se autocomenten y borren repetidamente sus comentarios en un post para hacerlo aparecer en la página principal.</li>
                <li>Se borrara o suspenderá a todos los usuarios que posteen que se van de la web, ya que eso es decisión de cada uno y genera bardo (en caso de querer irse avisar al admin para la completa eliminación de su cuenta).</li></ul>
<br />
<br />
				<strong>Se modifican o sancionan a los usuarios:</strong>
<br />
<br />
<ul>
				<li>Con avatares de mas de 200kb. de peso.</li>
				<li>Con avatares con contenido adulto.</li>
</ul>
